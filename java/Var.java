class Var {

    public static void main(String[] args) {
        final int NUM1 = 10;// ค่าเริ่มต้น = 10
        final int NUM2 = 20;// ค่าเริ่มต้น = 20
        /*
         * สามารถทำแบบนี้ก็ได้
         * public static void main(String[] args) {
         * final int NUM1;
         * final int NUM2;
         * NUM1= 100;
         * NUM2= 200;
         * 
         */

        System.out.println("ค่าตัวเลขที่ 1 = " + NUM1);// แสดงผลnum1
        System.out.println("ค่าตัวเลขที่ 2 = " + NUM2);// แสดงผลnum2

    }
}
