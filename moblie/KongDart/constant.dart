void main() {
  //! const เมื่อต้องการสร้างค่าคงที่ที่เรารู้ค่าแน่นอน
  //*ตัวแปรที่มีกำหนดค่าได้ครั้งเดียว และต้องมีการกำหนดค่าเริ่มต้นให้เสมอเมื่อประกาศ และไม่สามารถแก้ไขค่าได้

  //! final เมื่อต้องการสร้างค่าคงที่ที่เราจะรู้ค่าเมื่อ run

  int x = 1000;

//*const
  const int number1 = 100; //!ไม่ สามารถนำตัวแปรอื่นมาคำนวณเพิ่มได้
  // number1 = 500; //Error
  print(number1);

//final
  final int number2 = 150 + x; //สามารถนำตัวแปรอื่นมาคำนวณเพิ่มได้
  // number2 = 1000;
  print(number2);
}
